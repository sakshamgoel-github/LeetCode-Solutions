#include <bits/stdc++.h>
using namespace std;
#define turbo ios_base::sync_with_stdio(false);cin.tie(NULL);

class Solution {
public:
    int findComplement(int num) {
        long long x = 1;
        long long t = 2;
        while(num > x){
            x+=t;
            t = t*1LL*2;
        }
        return x-num;
    }
};

