#define turbo ios_base::sync_with_stdio(false);cin.tie(NULL);

class Solution {
public:
    int passThePillow(int n, int time) {
        time = time % (2*(n-1));
        if(time > (n-1)){
            return (2*n - time -1);
        }
        else{
            return (1+time);
        }
    }
};

